package com.smith.book.dto;

public class BCDTO {

	private String BCname;
	private String BChost;
	private String BCdate;
	private String BCtime;

	public String getBCname() {
		return BCname;
	}

	public void setBCname(String bCname) {
		BCname = bCname;
	}

	public String getBChost() {
		return BChost;
	}

	public void setBChost(String bChost) {
		BChost = bChost;
	}

	public String getBCdate() {
		return BCdate;
	}

	public void setBCdate(String bCdate) {
		BCdate = bCdate;
	}

	public String getBCtime() {
		return BCtime;
	}

	public void setBCtime(String bCtime) {
		BCtime = bCtime;
	}

}
